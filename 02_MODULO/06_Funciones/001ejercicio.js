//SUMA
const suma = function (primerNumero, segundoNumero) {
    return primerNumero + segundoNumero;
}
console.log(suma(4, 4));
let resultadoSuma = suma(10,5)
//RESTA
const resta = function (primerNumero, segundoNumero) {
    return primerNumero - segundoNumero;
}
console.log(resta(4, 4));
let resultadoResta = resta(5,1);
//MULTIPLICACION
const multiplicacion = function (primerNumero, segundoNumero) {
    return primerNumero * segundoNumero;
}
console.log(multiplicacion(4, 4));

//DIVISION
const division = function (primerNumero, segundoNumero) {
    return primerNumero / segundoNumero;
}
console.log(division(4, 4));

//REMANENTE
const remanente = function (primerNumero, segundoNumero) {
    return primerNumero % segundoNumero;
}
console.log(remanente(4, 4));

//EXPONENCIAL
const exponente = function (primerNumero, segundoNumero) {
    return primerNumero ** segundoNumero;
}
console.log(exponente(4, 4));